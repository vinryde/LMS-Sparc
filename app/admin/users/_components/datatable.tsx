// -----------------------------
// Zod schema for table typing
// -----------------------------
export const userSchema = z.object({
  id: z.string(),
  name: z.string().nullable().optional(),
  email: z.string().nullable().optional(),
  role: z.string(),
  // enrollment and courses can be arrays or objects (if counts used)
  enrollment: z.any().optional(),
  courses: z.any().optional(),
})

type UserRow = z.infer<typeof userSchema>

// -----------------------------
// Drag handle component (reused pattern)
// -----------------------------
function DragHandle({ id }: { id: string }) {
  const { attributes, listeners } = useSortable({
    id,
  })

  return (
    <Button
      {...attributes}
      {...listeners}
      variant="ghost"
      size="icon"
      className="text-muted-foreground size-7 hover:bg-transparent"
    >
      <IconGripVertical className="text-muted-foreground size-3" />
      <span className="sr-only">Drag to reorder</span>
    </Button>
  )
}

// -----------------------------
// RoleSelect component
// - shows Student & user only
// - calls UpdateUserRole server action
// - shows toast and refreshes on success
// -----------------------------
function RoleSelect({ userId, currentRole }: { userId: string; currentRole: string }) {
  const router = useRouter()
  const [value, setValue] = React.useState<string>(currentRole)

  async function handleChange(newRole: string) {
    // Only permit "Student" or "user" changes from this table.
    if (newRole === "Admin") {
      toast.error("Admin role cannot be assigned from this table")
      return
    }

    try {
      // call server action
      await UpdateUserRole(userId, newRole)
      toast.success("Role updated")
      setValue(newRole)
      // Ensure server-rendered page refreshes
      router.refresh()
    } catch (err: any) {
      console.error("UpdateUserRole error:", err)
      toast.error(err?.message ?? "Failed to update role")
    }
  }

  return (
    <Select value={value} onValueChange={(val) => handleChange(val)}>
      <SelectTrigger className="w-36" size="sm">
        <SelectValue placeholder={value} />
      </SelectTrigger>
      <SelectContent>
        {userRoles
          .filter((r) => r === "Student" || r === "user")
          .map((r) => (
            <SelectItem key={r} value={r}>
              {r}
            </SelectItem>
          ))}
      </SelectContent>
    </Select>
  )
}

